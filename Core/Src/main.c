int create_record(unqlite *pDb, const char *key, const char *value)
{
    int rc;
    rc = unqlite_kv_store(pDb, key, -1, value, strlen(value));
    if (rc != UNQLITE_OK)
    {
        fprintf(stderr, "Error creating record\n");
        return rc;
    }
    printf("Record created successfully.\n");
    return UNQLITE_OK;
}

int read_record(unqlite *pDb, const char *key)
{
    int rc;
    unqlite_int64 nBytes;
    rc = unqlite_kv_fetch(pDb, key, -1, NULL, &nBytes);
    if (rc == UNQLITE_OK)
    {
        char *value = malloc((size_t)nBytes);
        rc = unqlite_kv_fetch(pDb, key, -1, value, &nBytes);
        if (rc == UNQLITE_OK)
        {
            printf("Value for key '%s': %s\n", key, value);
        }
        else
        {
            fprintf(stderr, "Error reading record value\n");
        }
        free(value);
    }
    else
    {
        fprintf(stderr, "No record found for key '%s'\n", key);
    }
    return rc;
}

int update_record(unqlite *pDb, const char *key, const char *new_value)
{
    return create_record(pDb, key, new_value);
}

int delete_record(unqlite *pDb, const char *key)
{
    int rc = unqlite_kv_delete(pDb, key, -1);
    if (rc == UNQLITE_OK)
    {
        printf("Record deleted successfully.\n");
    }
    else
    {
        fprintf(stderr, "Error deleting record\n");
    }
    return rc;
}

int example_consumer(const void *pData, unsigned int nBytes, void *pUserData)
{
    printf("\r\nConsumer (Hex): ");

    for (unsigned int i = 0; i < nBytes; ++i) {
        printf("%02X ", ((const unsigned char *)pData)[i]);
    }

    printf("\r\nConsumer (String): %.*s\r\n", nBytes, (const char *)pData);
    return UNQLITE_OK;
}

int iterate(unqlite *pDb)
{
    unqlite_kv_cursor *pCursor;
    int rc;
    unqlite_int64 iData;

    rc = unqlite_kv_cursor_init(pDb, &pCursor);
    if (rc != UNQLITE_OK)
    {
        fprintf(stderr, "Error initializing cursor: %d\n", rc);
        return rc;
    }
    for (unqlite_kv_cursor_first_entry(pCursor); unqlite_kv_cursor_valid_entry(pCursor); unqlite_kv_cursor_next_entry(pCursor))
    {
        /* Consume the key */
        printf("================ SEPARATOR ================\r\n");
        printf("\r\nKey ==>");
        unqlite_kv_cursor_key_callback(pCursor, example_consumer, 0);

        /* Extract data length */
        unqlite_kv_cursor_data(pCursor, NULL, &iData);
        printf("\r\nData length ==>\r\n %lld", iData);

        /* Consume the data */
        printf("\r\nData Value ==>");
        unqlite_kv_cursor_data_callback(pCursor, example_consumer, 0);
    }
    unqlite_kv_cursor_release(pDb, pCursor);

    return rc;
}

int store_binary_data(unqlite *pDb, const char *key, const void *data, size_t dataSize)
{
    int rc = unqlite_kv_store(pDb, key, -1, data, dataSize);
    if (rc != UNQLITE_OK) {
        fprintf(stderr, "Error storing binary data: %d\n", rc);
        unqlite_close(pDb);
        return rc;
    }
    printf("Binary data stored successfully.\n");
    return UNQLITE_OK;
}
